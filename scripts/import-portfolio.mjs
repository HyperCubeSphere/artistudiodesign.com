#!/usr/bin/env node
// Walks /Users/vortex/Downloads/Mobiliere/<Folder>/<Project>/*.{jpg,jpeg},
// groups by project folder, writes 800-max-side q80 thumbs to
// public/images/portfolio/<cat>/<projectSlug>/NN.jpeg and the originals to
// <cat>/<projectSlug>/full/NN.jpeg via macOS `sips`. Emits a paste-ready
// TS array literal on stdout for src/content/portfolio.ts.
//
// Idempotent: skips outputs that already exist. Skips files matching
// `^WhatsApp` (stray exports).

import { mkdir, readdir, copyFile, access } from 'node:fs/promises'
import { spawnSync } from 'node:child_process'
import { dirname, join, basename } from 'node:path'

const SRC_ROOT = '/Users/vortex/Downloads/Mobiliere'
const DEST_ROOT = '/Users/vortex/WebstormProjectsPersonal/artistudiodesign.com/public/images/portfolio'

const FOLDER_TO_SLUG = {
  Baie: 'baie',
  Bucatarie: 'bucatarie',
  Dormitor: 'dormitor',
  Dressing: 'dressing',
  Living: 'living',
}

const CATEGORY_LABEL_RO = {
  baie: 'Baie',
  bucatarie: 'Bucătărie',
  dormitor: 'Dormitor',
  dressing: 'Dressing',
  living: 'Living',
}

const CATEGORY_LABEL_EN = {
  baie: 'Bathroom',
  bucatarie: 'Kitchen',
  dormitor: 'Bedroom',
  dressing: 'Dressing',
  living: 'Living room',
}

function exists(p) {
  return access(p).then(() => true, () => false)
}

function sips(args) {
  const r = spawnSync('sips', args, { encoding: 'utf8' })
  if (r.status !== 0) throw new Error(`sips failed: ${r.stderr}`)
  return r.stdout
}

function pixelDims(file) {
  const out = sips(['-g', 'pixelWidth', '-g', 'pixelHeight', file])
  const w = Number(out.match(/pixelWidth:\s*(\d+)/)?.[1])
  const h = Number(out.match(/pixelHeight:\s*(\d+)/)?.[1])
  return { w, h }
}

function slugifyProjectFolder(name) {
  // "Bucatarie 1" → "bucatarie-1"; "Dresing 2" → "dresing-2"; keeps source naming.
  return name.toLowerCase().replace(/\s+/g, '-')
}

async function processProject(category, projectSlug, projectDir) {
  const outDir = join(DEST_ROOT, category, projectSlug)
  const outFull = join(outDir, 'full')
  await mkdir(outFull, { recursive: true })

  const files = (await readdir(projectDir))
    .filter((f) => /\.(jpe?g)$/i.test(f))
    .filter((f) => !/^WhatsApp/i.test(f))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }))

  const images = []
  let idx = 0
  for (const file of files) {
    idx += 1
    const num = String(idx).padStart(2, '0')
    const src = join(projectDir, file)
    const thumbPath = join(outDir, `${num}.jpeg`)
    const fullPath = join(outFull, `${num}.jpeg`)

    if (!(await exists(fullPath))) {
      // Preserve original at q95 in a JPEG container (handles .jpg source too).
      sips(['-s', 'format', 'jpeg', '-s', 'formatOptions', '95', src, '--out', fullPath])
    }
    if (!(await exists(thumbPath))) {
      sips(['-Z', '800', '-s', 'format', 'jpeg', '-s', 'formatOptions', '80', src, '--out', thumbPath])
    }
    const { w, h } = pixelDims(thumbPath)
    images.push({
      src: `/images/portfolio/${category}/${projectSlug}/${num}.jpeg`,
      full: `/images/portfolio/${category}/${projectSlug}/full/${num}.jpeg`,
      width: w,
      height: h,
    })
  }
  return images
}

async function main() {
  const projects = []

  const sourceFolders = await readdir(SRC_ROOT, { withFileTypes: true })
  for (const folder of sourceFolders.filter((d) => d.isDirectory()).sort((a, b) => a.name.localeCompare(b.name))) {
    const category = FOLDER_TO_SLUG[folder.name]
    if (!category) continue
    const catDir = join(SRC_ROOT, folder.name)
    const projectFolders = (await readdir(catDir, { withFileTypes: true }))
      .filter((d) => d.isDirectory())
      .sort((a, b) => a.name.localeCompare(b.name, undefined, { numeric: true }))

    let projectN = 0
    for (const proj of projectFolders) {
      projectN += 1
      const slug = slugifyProjectFolder(proj.name)
      const projectDir = join(catDir, proj.name)
      const images = await processProject(category, slug, projectDir)
      if (images.length === 0) continue
      projects.push({
        id: `${category}-${slug}`,
        category,
        slug,
        projectN,
        photoCount: images.length,
        coverIdx: 0,
        images,
      })
      process.stderr.write(`✓ ${category}/${slug} (${images.length} photos)\n`)
    }
  }

  // Emit TS literal.
  const lines = []
  lines.push('// Auto-generated by scripts/import-portfolio.mjs')
  lines.push("import type { PortfolioCategorySlug } from '../i18n/config'")
  lines.push("import type { ImageRef } from './images'")
  lines.push('')
  lines.push('export interface PortfolioProject {')
  lines.push('  id: string')
  lines.push('  category: PortfolioCategorySlug')
  lines.push('  slug: string')
  lines.push("  /** Display title; defaults to a generic '<Category> · Proiect N' label. */")
  lines.push('  title: { ro: string; en: string }')
  lines.push('  cover: ImageRef')
  lines.push('  images: ImageRef[]')
  lines.push('}')
  lines.push('')
  lines.push('export const portfolioProjects: PortfolioProject[] = [')

  for (const p of projects) {
    const titleRo = `${CATEGORY_LABEL_RO[p.category]} · Proiect ${String(p.projectN).padStart(2, '0')}`
    const titleEn = `${CATEGORY_LABEL_EN[p.category]} · Project ${String(p.projectN).padStart(2, '0')}`
    const altRo = `${CATEGORY_LABEL_RO[p.category]} pe comandă — Arti Studio Oradea (proiect ${p.projectN})`
    const altEn = `Custom ${CATEGORY_LABEL_EN[p.category].toLowerCase()} — Arti Studio Oradea (project ${p.projectN})`
    lines.push('  {')
    lines.push(`    id: '${p.id}',`)
    lines.push(`    category: '${p.category}',`)
    lines.push(`    slug: '${p.slug}',`)
    lines.push(`    title: { ro: ${JSON.stringify(titleRo)}, en: ${JSON.stringify(titleEn)} },`)
    const coverImg = p.images[p.coverIdx]
    lines.push(`    cover: { src: '${coverImg.src}', full: '${coverImg.full}', width: ${coverImg.width}, height: ${coverImg.height}, alt: { ro: ${JSON.stringify(altRo)}, en: ${JSON.stringify(altEn)} } },`)
    lines.push('    images: [')
    for (const img of p.images) {
      lines.push(`      { src: '${img.src}', full: '${img.full}', width: ${img.width}, height: ${img.height}, alt: { ro: ${JSON.stringify(altRo)}, en: ${JSON.stringify(altEn)} } },`)
    }
    lines.push('    ],')
    lines.push('  },')
  }

  lines.push(']')
  lines.push('')
  lines.push('export function projectsByCategory(slug: PortfolioCategorySlug): PortfolioProject[] {')
  lines.push('  return portfolioProjects.filter((p) => p.category === slug)')
  lines.push('}')
  lines.push('')
  lines.push('export function projectBySlug(category: PortfolioCategorySlug, slug: string): PortfolioProject | undefined {')
  lines.push('  return portfolioProjects.find((p) => p.category === category && p.slug === slug)')
  lines.push('}')

  process.stdout.write(lines.join('\n') + '\n')
  process.stderr.write(`\nTotal: ${projects.length} projects\n`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
